import math

cateto1 = float(input("Digite o primeiro cateto: "))
cateto2 = float(input("Digite o segundo cateto: "))

hipotenusa = math.sqrt(cateto1**2 + cateto2**2)

print(f"A hipotenusa do triângulo é {hipotenusa:.2f}")
